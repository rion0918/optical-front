// 3ヶ月分のアイテムを生成
function generateScheduleMockItems() {
  const currentYear = 2025;
  const currentMonth = 12;

  const items = [];

  // 前月、今月、来月の3ヶ月分を生成
  for (let i = -1; i <= 1; i++) {
    let month = currentMonth + i;
    let year = currentYear;

    if (month < 1) {
      month += 12;
      year -= 1;
    } else if (month > 12) {
      month -= 12;
      year += 1;
    }

    items.push(...generateMonthlyItems(year, month));
  }

  return items;
}

